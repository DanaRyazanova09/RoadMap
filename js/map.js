$(document).ready(function() {
   var clickicon = $('.icon');
   var showsquare = $('.offsquare');
   clickicon.mouseenter(function () {
       showsquare.show();
   });

    clickicon.mouseleave(function () {
        showsquare.hide();
    });
}); 

